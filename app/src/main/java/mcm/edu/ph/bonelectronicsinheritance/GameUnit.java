package mcm.edu.ph.bonelectronicsinheritance;

public class GameUnit {

    int HP;
    int MP;
    int DEF;
    int minDMG;
    int maxDMG;

    public GameUnit(){}
    public GameUnit( int HP, int MP, int DEF, int minDMG, int maxDMG){

        this.HP  = HP;
        this.MP  = MP;
        this.DEF = DEF;
        this.minDMG = minDMG;
        this.maxDMG = maxDMG;


    }



}
